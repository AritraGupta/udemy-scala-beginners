package lectures.part1basics

object ValuesvariablesTypes extends App {

  val x: Int = 2
  println(x)

  //Vals are immutable
  //x = 3
  val y = 42
  println(y)

  val aString: String = "hello"
  println(aString)

  val aBoolean: Boolean = true
  val aChar: Char = 'A'
  val anInt: Int = x
  val aShort: Short = 2345
  val aLong: Long = 47381920327391L
  val aFloat: Float = 2.9f
  val aDouble: Double = 3.14

  //variables

  var z: Int = 4

  z = 5

  println(z)




}
